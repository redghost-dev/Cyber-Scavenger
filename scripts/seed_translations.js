const seedTranslations = async () => {
  try {
    // Connect to DB
    // Note: MONGODB_URI might need to be set explicitly if .env is not picked up correctly from scripts folder
    const uri = process.env.MONGODB_URI || 'mongodb://localhost:27017/cyber-scavenger';
    await mongoose.connect(uri);
    console.log('MongoDB Connected');

    for (const [lang, data] of Object.entries(translations)) {
      let translation = await Translation.findOne({ language: lang });
      
      if (translation) {
        console.log(`Updating ${lang}...`);
        // Merge existing with new (seed data takes precedence here for initialization)
        // For Mixed type, we need to update the object directly
        translation.data = { ...translation.data, ...data };
        translation.markModified('data');
      } else {
        console.log(`Creating ${lang}...`);
        translation = new Translation({
          language: lang,
          data: data
        });
      }
      
      await translation.save();
    }

    console.log('Translations seeded successfully');
    process.exit(0);
  } catch (err) {
    console.error(err);
    process.exit(1);
  }
};

seedTranslations();
